package cn.edu.cqupt.ok.exception;

public class ResearchFormException extends Exception {

	private static final long serialVersionUID = 1L;

	public ResearchFormException() {
		super();
	}

	public ResearchFormException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ResearchFormException(String message, Throwable cause) {
		super(message, cause);
	}

	public ResearchFormException(String message) {
		super(message);
	}

	public ResearchFormException(Throwable cause) {
		super(cause);
	}

	
}
